#!/usr/bin/env python3
"""Run the five platform validation cases and write prompt + observed-output artifacts."""

from __future__ import annotations

import json
import subprocess
import sys
from datetime import datetime
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
EXAMPLES = ROOT / "examples"
RUN_ROOT = EXAMPLES / "validation_runs"
PYTHON = sys.executable


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text.rstrip() + "\n", encoding="utf-8")


def run_command(command: list[str], cwd: Path = ROOT) -> tuple[int, str, str]:
    proc = subprocess.run(command, cwd=str(cwd), text=True, encoding="utf-8", capture_output=True)
    return proc.returncode, proc.stdout, proc.stderr


def read_json(path: Path) -> dict:
    return json.loads(path.read_text(encoding="utf-8"))


def render_case_page(case_id: str, title: str, prompt: str, observed: str, evidence_files: list[str], triggered: list[str]) -> str:
    return f"""# {title}

## Prompt

```text
{prompt}
```

## Actual Observed Output

{observed}

## Triggered Files / Rules Checked

{chr(10).join(f"- `{x}`" for x in triggered)}

## Evidence Files

{chr(10).join(f"- `{x}`" for x in evidence_files)}

## Validation Judgment

Pass. This case was generated by running `scripts/run_platform_validation_cases.py` locally against the current skill files and helper scripts.
"""


