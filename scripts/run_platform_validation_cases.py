def diagnose_case(case_dir: Path, prompt: str, items: list[str], triggered: list[str]) -> tuple[dict, list[str]]:
    write_text(case_dir / "prompt.txt", prompt)
    out_json = case_dir / "diagnosis.json"
    out_md = case_dir / "diagnosis.md"
    command = [
        PYTHON,
        str(ROOT / "scripts" / "diagnose_geo_inputs.py"),
        "--out-json",
        str(out_json),
        "--out-md",
        str(out_md),
        "--items",
        *items,
    ]
    code, stdout, stderr = run_command(command)
    write_text(case_dir / "command.txt", " ".join(command))
    write_text(case_dir / "stdout.txt", stdout or "(empty)")
    write_text(case_dir / "stderr.txt", stderr or "(empty)")
    if code != 0:
        raise RuntimeError(f"diagnose case failed: {stderr}")
    for rel in triggered:
        if not (ROOT / rel).exists():
            raise FileNotFoundError(rel)
    return read_json(out_json), [
        str((case_dir / "prompt.txt").relative_to(ROOT)).replace("\\", "/"),
        str(out_json.relative_to(ROOT)).replace("\\", "/"),
        str(out_md.relative_to(ROOT)).replace("\\", "/"),
        str((case_dir / "command.txt").relative_to(ROOT)).replace("\\", "/"),
    ]


